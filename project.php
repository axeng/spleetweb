				<h1>Bienvenue dans SPLEET !</h1>
				<br />
				Spleet est un jeu qui partage réflexion et action dans un univers futuriste et aseptisé. Vous contrôlez un patient qui a subi un grave traumatisme cérébrale suite un accident de voiture. Il se retrouve bloqué dans son propre subconscient. Un certain docteur dénommé Winston a une solution et vous guidera au sein de votre espace cérébrale via à une de ses inventions. Son invention est un moyen de modéliser le cerveau entier d’un humain sous la forme d’un univers. 
				<br />
				Le but du jeu sera de reconnecter les liaisons cérébrales du patient en résolvent des objectifs au cours des niveaux en passant d’un univers à l’autre. 
				<br />
