				<h2>Narration:</h2>
				<br />
				Vous contrôlez un patient qui a subi un grave traumatisme cérébrale suite un accident de voiture. Il se retrouve bloqué dans son propre subconscient. Un certain docteur dénommé Winston a une solution et vous guidera au sein de votre espace cérébrale via à une de ses inventions. Il nous informe qu’avec un de ses collègue et ami nommé le docteur Churchill, ils ont travaillé sur un projet de grande envergure: un moyen de modéliser le cerveau entier d’un humain sous la forme d’un univers où le patient pourra se déplacer dans son subconscient. 
				<br />
				Le but du jeu sera de reconnecter les liaisons cérébrales du patient en résolvent des objectifs au cours des niveaux en passant d’un univers à l’autre. 
				<br />
				Winston est notre guide, tout au long de l’aventure il nous informe de la situation. De plus, il nous conseille quand on se trouve devant une impasse.
				<br />
				Pour que le docteur communique et nous guide tout au long du jeu, Hugo (vanilor) a eu l’idée d’inclure d’un programme afin de pouvoir y mettre des sous titres et des fichiers audio en même temps.
				<br />
				Nous avons donc pu placer les sous-titres aux endroits clés des niveaux.
				<br />
				Ces sous-titres sont fondamentales pour donner une profondeur scénaristique mais aussi pour prévenir la frustration du joueur si il se sentait perdu et ne savaient pas quoi faire dans le niveau.
				<br />
