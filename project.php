				<h2>Un univers divisé en deux !</h2>
				<br />
				Lors de l’accident du héros, les deux hémisphères du cerveau de celui-ci se trouvent séparés. La partie gauche du cerveau, liée à la réflexion est donc complètement coupée de la partie droite, celle de la capacité de se mouvoir, l’agilité.
				<br />
				Dans les niveaux « de gauche » qui symbolisent la réflexion, le joueur va devoir chercher des boutons disséminés dans celui-ci cachés par différents procédés, dont notamment des énigmes. Vous serez équipé d’un pistolet laser qui permettra d’activer les boutons.
				<br />
				Dans les niveaux « de droite » qui symbolisent l’action, le joueur va cette fois tirer parti de ses réflexes et de son talent dans les jeux de plates-formes : en effet, ces niveaux seront constitués d’une suite de plates-formes avec un rythme de défilement élevé et truffés de pièges de différents types.
				<br />
				SPLEET est un véritable défi! Il se démarque des autres jeux grâce à ses deux types de jouabilité qui se rencontrent dans un seul et même monde.
				<br />
				<br />
				<br />
				<br />
				<br />
				<h2>Un jeu avec une vraie identité graphique!</h2>
				<br />
				Les deux espaces « gauche » et « droite » ont une identité graphique différente. Spleet se distingue de la grande majorité des jeux vidéo par sa patte graphique très personnelle où chaque élément garde le même esprit graphique mais en même temps possède deux styles graphiques différents entre le côté action et le côté réflexion.
				<br />
